'''Python lists come with a variety of built-in methods 
that allow for common list manipulations. One such 
operation is determining the index of an item in the list.

Given a list:

Copy Code
fruits = ["apple", "banana", "cherry", "peach", 
"watermelon"]

How would you determine the index of the fruit "cherry" 
in this list?'''

#Answer

fruits = ["apple", "banana", "cherry", "peach", 
"watermelon"]

print(fruits.index("cherry"))

